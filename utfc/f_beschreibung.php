			die Flaschen in der Mitte zu treffen, die mangelnde Fähigkeit, die Flaschen schnell und ordentlich zu leeren - und die Schiedsrichter. Diese können bei Verstößen gegen die Regeln Strafbiere verhängen. Gewinner 
			einer Partie ist die Mannschaft, die es geschafft hat, all ihre Flaschen (inklusive der evtl. gesammelten Strafbiere) zuerst bis auf maximal drei Tropfen zu leeren und beim Schiedsrichter abgegeben hat.
			Weitere Details ergeben sich beim Lesen der Regeln. Es gibt durchaus noch andere Varianten dieses Spiels. So wird teilweise mit vier Spielern pro Mannschaft, mit 0,5 Liter-Dosen statt 0,33 Liter-Flaschen 

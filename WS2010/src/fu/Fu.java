package fu;

public class Fu {

	private String funame;
	private Fara fara;

	public void fu(String s) {
		this.funame = s;
	}

	public String getFuname() {
		return funame;
	}

	public void setFuname(String funame) {
		this.funame = funame;
	}

	public Fara getFara() {
		return fara;
	}

	public void setFara(Fara fara) {
		this.fara = fara;
	}

}

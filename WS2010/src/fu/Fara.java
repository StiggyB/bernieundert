package fu;

public class Fara {

	private String faraname;
	private Fu fu;

	public void fu(String s) {
		this.faraname = s;
	}

	public String getFuname() {
		return faraname;
	}

	public void setFuname(String funame) {
		this.faraname = funame;
	}

	public Fu getFu() {
		return fu;
	}

	public void setFu(Fu fu) {
		this.fu = fu;
	}
}